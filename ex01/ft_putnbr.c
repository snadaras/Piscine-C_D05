#include <unistd.h>

void	ft_putchar(char c);

void	ft_putnbr(int nb)
{
	int topmin;

	topmin = 0;
	if (nb == -2147483648)
	{
		nb = nb / 10;
		topmin = 1;
	}
	if (nb < 0)
	{
		ft_putchar('-');
		nb = -nb;
	}
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else
		ft_putchar(nb + '0');
	if (topmin == 1)
		ft_putchar(8 + '0');
}
