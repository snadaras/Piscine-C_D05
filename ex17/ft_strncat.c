char	*ft_strncat(char *dest, char *src, int nb)
{
	int	i;
	int	k;

	i = 0;
	k = 0;
	while (dest[k])
		k++;
	while (src[i] && i < nb)
	{
		dest[k] = src[i];
		k++;
		i++;
	}
	dest[k] = '\0';
	return (dest);
}
